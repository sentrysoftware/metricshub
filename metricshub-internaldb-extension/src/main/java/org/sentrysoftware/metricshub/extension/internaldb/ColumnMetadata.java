import lombok.AllArgsConstructor;
import lombok.Data;

/**
 * Metadata for a column in a SQL table.
 */
@Data
@AllArgsConstructor
public class ColumnMetadata {

	/**
	 * 1-based index for PreparedStatement
	 */
	private int position;
	/**
	 * Java type for the column
	 */
	private Class<?> javaType;
	/**
	 * SQL type for the column java.sql.Types.*
	 */
	private int sqlType;
}
